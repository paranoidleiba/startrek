#this is the countdown to land Spock on the planet, this function will be used below
def countdown_spok():
    print(f"\n{Fore.YELLOW}Waiting for 5 Earth seconds while Spock moves to the planet.")
    for seconds_left in range(5, 0, -1):
        print(f"\r{seconds_left} seconds left", end="", flush=True)
        time.sleep(1)
    print(f"\n{Fore.GREEN}Spock on the planet")

